def get_recommendations(user_data_dict, model, le_dict, target_le, feature_columns):
    """
    Given a raw user data dictionary, returns top 3 recommendations.
    user_data_dict should have keys matching feature_columns.
    Returns: (top_cat, top_cat_prob, top_careers, top_probs)
    """
    # 1. Convert dict to input array
    input_list = [user_data_dict[col] for col in feature_columns]
    input_array = np.array(input_list).reshape(1, -1)

    # 2. Prediction Probs
    probs = model.predict_proba(input_array)[0]

    # 3. Calculate Category Confidences
    cat_confidences = {}
    career_names_from_model_classes = target_le.inverse_transform(model.classes_)

    for i, prob in enumerate(probs):
        career_name = career_names_from_model_classes[i]
        cat_name = REVERSE_CAREER_MAP.get(career_name, "Other")
        cat_confidences[cat_name] = cat_confidences.get(cat_name, 0) + prob

    # 4. Sort categories
    sorted_cats = sorted(cat_confidences.items(), key=lambda x: x[1], reverse=True)
    top_cat, top_cat_prob = sorted_cats[0]

    # 5. Get global top 3 specific careers
    top_indices = np.argsort(probs)[-3:][::-1]
    top_careers = []
    top_probs = []

    for i in top_indices:
        class_label = model.classes_[i]
        career_name = target_le.inverse_transform([class_label])[0]
        top_careers.append(career_name)
        top_probs.append(probs[i])

    return top_cat, top_cat_prob, top_careers, top_probs

