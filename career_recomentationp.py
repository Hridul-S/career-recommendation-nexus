def get_user_input_dict(le_dict, feature_columns):
    # This is a renamed version of the old get_user_input that returns a dict
    print("\n" + "="*30)
    print(" CAREER PREDICTION SYSTEM ")
    print("="*30)
    print("Please provide your details for recommendation:\n")

    user_data = {}
    
    # Define skill columns that should use text input (Weak/Average/Strong)
    skill_columns_list = ['Python', 'SQL', 'Java']
    
    for col in feature_columns:
        if col in skill_columns_list:
            # Ordinal skill feature
            options = list(SKILL_MAP.keys())
            print(f"\n--- {col} (Skill Level) ---")
            print(f"Levels: {', '.join(options)}")
            while True:
                choice = input(f"Enter {col} (Weak/Average/Strong): ").strip()
                # Case-insensitive match for skills
                matched_skill = next((k for k in SKILL_MAP if k.lower() == choice.lower()), None)
                if matched_skill:
                    user_data[col] = SKILL_MAP[matched_skill]
                    break
                else:
                    print("Invalid input. Please choose from: Weak, Average, Strong.")
        elif col in le_dict:
            # Categorical feature
            le = le_dict[col]
            options = sorted(list(le.classes_))

            print(f"\n--- {col} ---")
            print(f"Options: {', '.join(options)}")

            while True:
                choice = input(f"Enter {col} (or type 'search' to find options): ").strip().lower()

                if choice == 'search':
                    keyword = input("Enter keyword to search for: ").lower()
                    matches = [o for o in options if keyword in o.lower()]
                    if matches:
                        print(f"Matching options: {', '.join(matches)}")
                    else:
                        print("No matches found.")
                    continue

                # 1. Try Exact Case-Insensitive Match
                matched_option = next((o for o in options if o.lower() == choice), None)
                
                # 2. Try Space-Normalized Match (e.g., 'cyber security' -> 'cybersecurity')
                if not matched_option:
                    choice_no_space = choice.replace(" ", "")
                    matched_option = next((o for o in options if o.lower().replace(" ", "") == choice_no_space), None)
                    if matched_option:
                        print(f"Auto-selected closest match: {matched_option}")

                # 3. Try Partial Match if no exact/normalized match
                if not matched_option and len(choice) > 1:
                    partial_matches = [o for o in options if choice in o.lower()]
                    if len(partial_matches) == 1:
                        matched_option = partial_matches[0]
                        print(f"Auto-selected closest match: {matched_option}")
                    elif len(partial_matches) > 1:
                        print(f"Multiple matches found: {', '.join(partial_matches)}")
                        print("Please be more specific or type the full option.")
                        continue

                if matched_option:
                    user_data[col] = le.transform([matched_option])[0]
                    break
                else:
                    print(f"Invalid input. Please choose a valid {col}. Type 'search' if you're unsure.")
        else:
            # Numerical feature
            while True:
                try:
                    val = input(f"Enter {col} (numeric): ").strip()
                    user_data[col] = float(val)
                    break
                except ValueError:
                    print(f"Invalid input. Please enter a numeric value for {col}.")
    return user_data

